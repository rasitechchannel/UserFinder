function start() {
    // Tampilkan judul dengan Figlet
    figlet('UserFinder', (err, data) => {
        if (!err) {
            console.log(chalk.yellow(data));
            displayMenu();

            rl.question("Select an option (1/2/3): ", (choice) => {
                if (choice === '1') {
                    rl.question("Enter username to check: ", (username) => {
                        checkUsernameAvailability(username);
                    });
                } else if (choice === '2') {
                    about();
                    start();
                } else if (choice === '3') {
                    console.log("Exiting...");
                    rl.close();
                } else {
                    console.log("Invalid choice. Please select a valid option.");
                    start();
                }
            });
        } else {
            console.error("Error rendering Figlet text.");
        }
    });
}

